<script>
export default {
  layout: "test",
  data() {
    return {
      // NavBar Start
      // NavBar End
    };
  },
  methods: {
    // NavBar Start
    scroll() {
      window.onscroll = () => {
        var navbar = document.getElementById("navbar");
        var sticky = navbar.offsetTop;
        if (window.pageYOffset >= sticky) {
          navbar.classList.add("sticky");
        } else if (window.pageYOffset <= sticky) {
          navbar.classList.remove("sticky");
        }
      };
    }
    // NavBar End
  },
  mounted() {
    // NavBar Start
    this.scroll();
    // NavBar End
  }
};
</script>
